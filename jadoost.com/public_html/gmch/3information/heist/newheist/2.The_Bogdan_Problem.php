<br><br>
공장 입구의 짧은 복도를 지나면 제어실(?)로 들어가게 되는데 뉴비들은 여기서 닥돌하다가 1초 만에 순삭 되거나 스낵을 낭비한다. 그래서 나온 방법이 입구를 개인 차량으로 막고 뒷문으로 들어가는 건데 시간도 오래 걸리고 어차피 적들은 계속 밀고 들어오기 때문에 효율적이지도 않다.
<br><br>
그래서 입구의 짧은 복도에 정찰 팀을 모셔놓고, 제어실로 나갈 때 고개만 빼꼼히 내밀면 적들이 몇 명 시야에 들어온다. 재빠르게 헤드만 따주면 피해 없이 제거가 가능하다. 후에 멀리 2층에 머리만 보이거나 서있는 애들 2명이 있는데 얘들이 상당히 아프다. 재빠르게 헤드샷으로 제거한다. 제거한 후 살짝 전진하면 오른쪽에 창문을 통해 엄폐하고 있는 적을 하나 잡을 수 있다. 여기까지 잡고 나면 제어실 안에서는 안전해진다. 이후 천천히 쪼으면서 직진하면 제어실 바로 앞에 숨어있는 놈이 시야에 들어오는데 간 보면서 잡아주면 피해 없이 제거 가능하다. 그리고 난간을 넘어서 직진하는데, 엄폐하고 있는 놈이 왼쪽에 있고 2층에서 한 놈이 내려오는데 재빠르게 잡아준다. 그리고 2층으로 올라가면 정면에서 한 놈, 오른쪽에서 한 놈 나오니 헤드샷이나 어썰트 샷건으로 정리해 주고, 건너편에 보면 연기 때문에 잘 안 보이지만 한 놈이 엄폐하고 있다. 헤드로 정리해도 되고 알라봉으로 날려도 된다. 그리고 모퉁이를 돌면 나오는 한 놈을 제거하면 위 층으로 올라갈 수 있는데 이때 1층에 적들이 들어와서 쏠 수도 있지만 무시하고 올라가면 된다.
<br><br>
위층에 올라가면 적들을 정리하고 ULP를 데리고 나온다. ULP 구출 후에는 최상층에서 공장 내부를 한눈에 볼 수 있는데 미니건으로 몰살을 시켜준다. 나오는 길에는 적들의 수가 적기 때문에 크게 죽을 일은 없다. 이후에는 아래의 공략을 따르면 된다.
