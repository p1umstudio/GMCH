<dl>
    <dt>정찰 팀</dt>
    <dd>
        쉬운 꿀보직이다. 신습격 지능범죄를 할 때 초보들은 이 보직을 추천한다. 지상 팀이 ULP 요원을 구출해 나오는 동안 근처의 급수탑 위에 올라가 몰려오는 적들을 막아야 한다. 역시나 적들이 꽤 많이 몰려오지만 급수탑 난간에 엄폐할 수 있으니 지상의 적들은 별로 문제가 되지 않으나[11] 중간중간 공격 헬기들이 등장해 머신건을 갈기므로 주의. 그리고 급수탑에 굳이 올라가지 않아도 정상적으로 진행이 되는데, 차량에 타고 있으면 머신건 대신 미사일을 날린다. 따라서 구루마를 탔다면, 헬기가 미사일을 발사하기 전에 먼저 제거해야만 하고, 그럴 수 없으면 추천되지 않는다. 사람에 따라 어느 정도 총알을 막아주고 버자드의 미사일까지 막아주며, 속도도 어느 정도 되는 인서전트나 인서전트 픽업 커스텀을 타는 게 쉬울 수도 있다.
<br><br>
하프트랙을 지상팀이 들어간 입구에 뒤로 주차하고 포탑에 타서 몰려오는 적들을 상대해도 좋다. 포탑앞은 방탄이고 포탑뒤로는 적이 잘 오지 않으며, 오더라도 포탑을 놓고 뒤로 돌아서 총으로 쏘면 뒤에 엄청난 물량에게 맞지 않고 제거할 수 있다. 단 하프트랙은 속도가 느리다는 게 단점.
<br><br>
탬파(무기화)를 다루는데 능숙한 유저라면 날아오는 버자드는 그때그때 사거리를 확보하여 듀얼 미니건으로 사격하면 상대방 미사일이 발사되기 전에 쉽게 격추시킬 수 있으며, 몰려오는 지상 보병들은 탬파(무기화) 앞에서는 속수무책이다. 다만 탬파가 방탄성능이 미미하므로 이 점만 조심하면 된다. 탬파를 다루는데 능숙한 플레이어들에게만 추천되는 방법.
<br><br>
정말 어떤 차도 없는데, 주물 공장 안으로 들어가는 것이 꺼려진다면, 차고가 높은 6인승 트럭 등을 이용해 주물 공장 진입로 앞 혹은 뒷편으로 돌아가거나 방지턱을 넘어가서 주차한 다음, 그것을 엄폐물 삼아 버티는 방법도 있다. 다인승 트럭이나 저코니움 저니같은 밴만 있으면 바로 지상 팀 탈출과 동시에 차량에 탑승하여 도주할 수 있는 것도 장점. 또한 차량에 탑승한 상태가 아닌 뒤에 기대고 있는 상태이기에 버자드가 미사일을 발사하지 않는다. 따라서 지상 팀이 나오기 전까지만 버자드를 처리해두면 탈출 역시 수월하게 진행된다.
<br><br>
물론 가장 쉬운 방법은 지원 팀도 주물 공장 안으로 들어가서 기다리는 것이다. 완전히 내부로 들어가면 구역을 벗어났다고 뜨지만 공장 내부와 입구 사이에 있는 짧은 복도에 있으면 구역을 벗어나지 않으면서도 안전하게 있을 수 있다. 간간이 문을 열고 들어오는 적들만 처치해주면 쉽게 할 수 있다. 이후 지상 팀이 ULP를 입구로 데리고 오면 ULP 혼자 문밖으로 나가서 총에 맞다가 죽는다. 그러면 실패가 뜨며 체크포인트가 다른 곳으로 바뀌어 앞의 인서전트 픽업을 타거나 도로에 있는 개인 차량을 타고 도주하면 쉽게 클리어가 가능하다. 지능 범죄가 아니라면 그냥 유저가 공장 문까지 ULP를 데리고 가서 자살하면 빠르게 재시작이 가능하다.
<br><br>
주물 공장에 지원 팀까지 들어가는 방법을 썼다면 연장선상에 있는 꼼수가 있는데, 주물 공장 입구 앞에 방탄 구루마를 주차하고 들어가는 것. 공터 쪽은 차가 지나갈 수 없게 막혀 있지만, 왼쪽으로 돌아서 골목으로 들어가면 문 바로 앞까지 접근할 수 있다. ULP를 데리고 나오기 전에 지원 팀이 먼저 차량 운전석에 탑승한 뒤 정면의 적을 제거해 주고 지상 팀을 태워 도주하면 재시작할 필요 없이 탈출할 수 있다. 하지만 구루마에 탑승하는 순간부터 버자드가 미사일을 쏘니 주의. 정 불안하면 방탄 구루마 대신 나이트샤크나 인픽커를 이용하는 방법도 있다. 나이트샤크나 인픽커의 경우 차체 출력이 높기 때문에 주물 공장 입구 앞에 설치된 작은 바리케이드 정도는 쉽게 넘어갈 수 있다. 이후 차량을 엄폐물 삼아 입구를 가리면 된다. 참고로 굳이 방향은 바꿀 필요 없다. 정찰팀이 주물 공장 안에 들어가 있으면 밖의 적들이 처리되지 않아 수많은 적 NPC가 대기하고 있는데, 이 방법을 이용하면 최대한 사각을 주지 않고 안전하게 차량에 탑승할 수 있으며, ULP를 죽여야 하는 번거로움을 감수할 수 있다. 이후 처음 진입했던 곳으로 되돌아가지 말고 건물 뒤편으로 가면 왼쪽에 삼각형 모양의 작은 틈이 있는데, 인픽커도 어렵지 않게 통과할 수 있는 크기다. 이 구멍으로 나가면 스무스하게 진행할 수 있다.


