2. 헤비 스나이퍼 라이플 MK2의 열감지 스코프를 사용한다.
헤비 스나이퍼 라이플 MK2에 달리는 열감지 스코프는 장애물이 있더라도 그걸 투과해서 적의 위치를 볼 수 있으며 맨몸은 더욱 밝게 빛나 구분하기 매우 쉽다. 처음 몸을 숨기는 곳에서 서두르지 않고 하나씩 처리해주면 방탄복 한두 개 쓰고 끝날 정도로 난이도가 심각하게 급락한다. 벙커를 구입하고 열감지 스코프를 연구한 사람은 적극적으로 이용하자. 하지만 폭발탄은 쓰지말자. NPC하나 잡는데 머리를 맞춰도 두발이 필요하고 잘못 맞추면 세발 이상 쏴야하는데 NPC는 20명이 넘게나오는데 잔탄수가 40발 밖에 없다. 반면에 일반탄은 머리를 맞추면 한 방에 죽일 수 있고, 탄알 수도 많으므로 훨씬 유리하다. 즉, 폭발탄이 일반탄보다 효율성이 떨어진다.
<br><br>
3. 자동 조준을 켜고 게임 패드를 사용한다.
게임 패드의 자동 조준이라면 제아무리 암흑 상태라도 자동조준이 알아서 적의 몸통을 조준해주고, 조금만 올리면 바로 헤드샷이지만, 스토리에서 설정 - 게임 패드에서 자동 조준을 직접 켜줘야 한다. 게임 패드를 가지고 있는 유저라면 강력추천하는 부분. 어벤져 뿐만 아니라 적이 다수 숨어있는 다른 실내 전투에서도 매우 유용하다.
<br><br>
4. 계단을 오르는 타이밍을 플레이어들끼리 어느 정도 맞추면 안전하다.
