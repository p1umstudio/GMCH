팁으로 ULP를 데리고 주물공장 입구까지 가면 ULP에게 길을 터주라는 문구가 사라지는데 이때 자살하거나 입구 밖으로 나간 ULP를 죽게 내버려 두면 주물공장 뒤편에 시작하게 된다. 이때 정면에 인서전트 픽업이 리스폰 되어 있으니 그걸 타고 가거나 혹은 그 뒤편에 타고 왔던 개인 차량이 리스폰 되어 있으니 본인이 편한 방법으로 ULP와 타고 맥켄지 비행장으로 가는 방법도 있다.</dd>
