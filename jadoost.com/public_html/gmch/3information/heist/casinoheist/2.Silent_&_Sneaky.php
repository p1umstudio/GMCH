다만 경비의 위치는 랜덤이므로 저 3가지 방법 중 하나만 고수해서는 안된다. 저 3가지 방법 중 현재 상황에 가장 알맞는 방법을 택해야 한다.
<br><br>
여기서도 대 사기극과 마찬가지로 강탈 후 금고 경보가 울리기 전에 나와야한다. 시간이 다되면 경보가 울리니 주의. 지하의 경비원은 비교적 쉽게 제압이 가능하니 제압하고 계단으로 가는것을 추천한다. 다만 듀건의 화물을 해놓았으며 훔칠게 얼마 남지 않았다면 엘리트 도전이 아닌 이상 챙기고 가는게 훨씬 낫다. 들켜서 총알을 맞아도 상품은 진짜 많아봐야 10만 달러내외로 손실되는데 그거 아낄려고 하나당 수십만 달러하는 물품을 놓치긴 아깝다. 그러나 듀건의 화물을 해놓지 않았다면 불가능에 가까운 일이니 깔끔하게 포기하던지, 가스 마시고 자살해서 리트하던지 하자.
<br><br>
사실 EMP를 사용하지 않고도 스텔스로 도망치는 게 가능한데, 맨트랩 바로 앞에서 감시 중인 녀석의 시야와 벽 사이에 딱 사람 한 명 지나갈 정도의 공간이 있다. 그러므로 최대한 벽에 붙어서 이동하면 안 들키고 해당 구간을 지나가는 게 가능하다. 유저들 사이에서 '벽타기' 라고 불리는 스킬이니 참고할 것. 다만 주의할 점은 중간에 기둥이 볼록하게 솟은 구간이 있는데, 여기서 그냥 문쪽으로 가겠다고 설쳤다가는 들킨다. 많은 뉴비들이 자주 하는 실수 구간. 보안요원의 부채꼴 시야가 가장 넓어지는 부분인데 직선경로를 침범하고 있기 때문에 그냥 달려선 안된다. 기둥을 지난 뒤 오른쪽으로 꺾어서 벽에 몸을 딱 대고 움직여야 안 들키니까 명심하자. 또 하나 주의할 점은 기둥을 통과하는 루트를 카메라가 비추고 있기 때문에 미적미적 통과하는 것이 불가능하다. 가급적 빠른 통과가 필수.
<br><br>
참고로 듀건 화물 다 깨놨어도 지하 구간에서 탈출 할 때는 될 수 있는 한 사람도 안 죽이는 쪽이 좋다. 버그인지는 알 수 없지만 누구한테도 들키지 않고 지하를 모조리 청소했어도 위로 올라오면 뜬금없이 시체가 발견되었다면서 알람이 울리는 경우가 많다. 가끔 안 울릴 때도 있지만 거의 6~70%는 울리니까 가능하면 아무도 안 죽이고 위에 언급한 방법으로 움직이는 게 낫다.
<br><br>
계단에 들어서면 카메라가 1개 있으니 무력화 시켜도 되지만 그냥 1인칭으로 달려서 무시하는게 훨씬 낫다. 윗층에 도달하면 별 5개가 뜨지만, 대 사기극과 마찬가지로 경비원들은 플레이어를 천천히 걸어다니며 수색할 뿐 위치를 알지 못한다. 팀원과 호흡을 맞추며 경비원들을 하나하나 제거하고 금속탐지기에 걸려도 아무런 일이 없으니 아무 출입구로 빠져나가면 된다.앞서 언급했던 EMP를 사용하지 않고 놔뒀다가 금고에서 나온뒤 사용할 수도 있다. 그러면 금고에서 나와 건물 탈출로로 나가기까지 원스톱으로 안전하고 빠른 진행이 가능하다.
