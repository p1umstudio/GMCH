카지노 내부에 조용히 숨어들어가 공략하는 방식이다. EMP와 야간 투시 장비를 구해서 사용할 수 있으며, 옥상으로 침투할 경우 헬기를 사용한다. 그러나 옥상으로 출입시 호텔층을 정리해야하니 쓰레기 처리장이나 뒷편의 직원 로비로 들어가는 방식을 추천한다.
<br><br>
나머지 두 방식과는 다르게 초심자, 비숙련자에게는 난이도가 상당히 높은 편이다. 오죽하면 레스터가 비밀 작전을 하면 머리털 나고 이렇게 어려운 작업은 처음이라고 말한다. 따라서 카지노 습격을 처음하는 생초보라면 비밀 작전 공략을 잘아는 고수와 같이 하거나, 아니면 다른 두 접근 방식을 해보고 카지노 지형지물이나 습격 진행 방식을 어느정도 파악하고 비밀 작전을 하는것을 추천한다. 공략을 보고, 각종 노하우를 익히면서 플레이해 보면 숙련도가 늘어나는 방식이다. 그리고 이 작업도 공격 전술과 동일하게 듀건의 화물을 모두 처리하거나, 최소한의 보호용 헬멧만 제거하면 난이도가 크게 감소한다.
<br><br>
무기로는 스턴 건과 소음기가 달린 권총이 기본으로 주어지며, 나머지는 자신이 고용하기에 달렸다. 모든 무기에 소음기가 달려있으므로 건물 내부에서 사격해도 발각될 위험이 없다. 준비 작업중 EMP와 야간 투시 장비를 얻을 수 있는데, 중간에 아주 요긴하게 쓰일수는 있으나, 없어도 충분히 클리어는 가능하다. 나노 드론을 사용해 경비원을 제압할 수도 있으나, 소음기 달린 권총으로도 충분히 제압이 가능하다.
<br><br>
건물 뒷편의 직원 로비나 쓰레기 처리장으로 들어가면 바로 앞에 금속탐지기가 있으니 금속탐지기 오른쪽의 두꺼비집을 스턴건으로 쏴서 잠시 무력화 시키고 들어가야한다. 들어간 후에는 시야에 있는 경비원들을 하나씩 처리하면 된다. 카메라의 경우에는 시야에 들어가자 마자 들키는건 아니고 시야 안에 어느정도 머물러 있을 경우엔 들킨다. 따라서 카메라를 굳이 무력화 시키거나 부수지 않아도 쉽게 진행 가능하다. 2개 이상 부수면 건물에 경보가 울린다. 카메라는 스턴 건으로 몇 십초간 무력화가 가능하니 꼭 필요하다 싶을 때 1개만 부수는 것이 좋다. 다만 한번에 카메라 여러개를 무력화 시킬 경우에도 발각되니 주의. 시체가 카메라에 잡혀도 경보가 울리니 주의해야한다.
<br><br>
가운데 로비 뒷편의 오피스를 점령하고 복도를 걸어다니는 경비원까지 제거했다면 안전하게 오늘의 금고(오금, 비밀금고)를 털 수 있다. 이후 계단으로 가면 중간에 카메라 1대와 경비원 1명이 올라오니 주의. 계단에 들어가는 순간 은신이 풀려 그냥 걷다간 발소리가 새어나가서 올라오는 경비원에게 발각될 수도 있다. 그러나 타이밍 상 경비가 올라오기 전에 경비 제거지점에 미리 도착해 있을 수 있기 때문에 실제로 은신을 쓰는 플레이어는 거의 없다.
<br><br>
계획 준비 중 EMP를 했다면 휴대폰을 열어 시큐어 에이전시 앱을 선택해 EMP를 가동시킬 수 있다. 약 1분정도 가동되며, 이때는 카메라가 전부 무력화되며 경비원들의 시야가 극도로 좁아진다. 아래부터는 주로 EMP를 사용하는 타이밍이 세가지로 나뉜다.
