        <dt>○경찰서 팀○</dt>
        <dd>
            주어진 무기가 권총과 야경봉 뿐이며, 복장 설정을 할 수 없다.
            <br><br>
            경찰 크루저를 훔쳐 수배를 푼 다음 경찰서로 가서 죄수 호송 버스 일정표를 훔쳐온다. 훔친 경찰 크루저는 목적지로 가져가 비치된 기름통을 이용해 파괴하고 계획실로 일정표를 가져오면 된다. 경찰서 팀은 경찰을 따돌리는데 익숙하면 쉽게 클리어 할 수 있다. 경찰 크루저를 훔치는 방법은 2가지가 있다.
            <br>
            첫 번째 방법은 사방에 총을 쏴 어그로를 끌어서 경찰차를 부르는 방법이다.
            <br>
            총질로 어그로를 끈 후에 경찰차가 오면 수배가 걸렸기 때문에 경찰이 바로 공격하므로 경찰을 사살한 다음 탈취하는 것이 좋다. 단, 경찰을 사살하면 즉시 별 3개가 뜨면서 헬기까지 뜨므로 주의 할 것. 좀 더 빠르게 훔치려면 차를 타고 총질하며 어그로를 끈 뒤 경찰차가 오길 기다리지 않고 근처에 스폰된 경찰차로 찾아가 사살한 뒤 훔치면 된다.
            <br><br>
            두 번째 방법은 1명이 핸드폰을 열어서 911에 전화해 경찰차를 호출한 뒤 훔치는 방법이 있다.
            <br>
            911로 경찰차를 호출 하면 잠시 뒤 경찰차 한대 가 오는데 그냥 내버려 두면 내리지 않고 왔다가 그냥 가버리기 때문에 경찰차 앞을 가로 막아 세운 뒤 타고 있던 경찰들을 끌어 내린 뒤 타고 도망치면 된다. 이렇게 하면 수배 별이 1개만 뜬다. 꼭 2명이 같은 경찰차를 탈 필요는 없으나 둘 다 탈 거면 동시에 뺏어 타는 것을 권장한다.
            <br><br>
            이 계획준비도 앞서 '탈옥 - 버스'에서 언급 되었듯 수배 별 버그가 생기기가 쉬운데 만약 2명이 같은 경찰차에 타고 시야에서 확실하게 벗어났는데도 수배레벨이 떨어지지 않는다면 두 사람 다 차에서 내리고 별이 깜빡일 때까지 기다렸다 타면 된다. 주의할 점은 이 두 사람이 나중에 탈옥 피날레에서 같은 비행기를 타게 되면 탈옥 - 피날레 항목에서 언급되는 그 멘붕 상황이 재현될 수 있다.
            <br><br>
            만약 어떤 사유로든 재시작한다면 베스푸치 볼버드에서 시작 하게 되는데 여기서 경찰을 따돌리기 좋은 장소로 4 인테그리티 아파트 옆 고속도로에 뚫린 스토리 모드에서 보석상 작업시 오토바이를 타고 도망쳤던 땅굴을 통해 숨을 수도 있고, 3 알타 St. 아파트 정면의 지하철 터널로 숨으면 매우 쉽게 경찰을 따돌릴 수 있다. 주의점은 두 곳 모두 초록색 발전기가 곳곳에 있는데 이걸 차로 들이받으면 그대로 폭발하니 조심하자.
            <br><br>
            수배가 풀리면 경찰차를 타고 경찰서로 간다.[14] 경찰서에 도착하면 절대 공격적인 행동을 하면 안 된다. 뛰는 것은 상관 없으나 다른 경찰과 부딪히는 즉시 수배가 걸리게 된다. 경찰서로 잠입해 일정표를 훔치면 나오는 도중 경찰 한 명이 플레이어 방향으로 다가오므로 잘 피해서 가도록 하자. 이후 다시 경찰차를 타고 지정된 위치를 따라 이동한 뒤 비치된 기름통을 차에 뿌린뒤 불을 붙여 경찰차를 폭파시키면 된다. 굳이 차 여기저기 뿌릴 필요 없이 엔진 쪽에만 조금 뿌린 뒤 불 붙여도 잘 터진다. 어떻게든 경찰차만 파괴하면 되기 때문에 물에 빠뜨려서 파괴해도 된다. 약간이라도 더 빠르게 클리어 하려면 기름통이 비치된 장소까지 갈 필요 없이 근처의 주유소에 경찰차를 대놓고 주유기를 향해 총을 쏴 함께 폭파 시켜도 된다. 주유소를 못찾겠다면 지정된 위치까지 가서 경찰차를 기름통 바로 옆에 세우고 기름통을 총으로 쏴서 폭파시킬 수도 있다. 폭파시키기 전에 미리 개인 차량 요청을 해둔 다음 터뜨리면 편하다. 공방에서 습격을 진행할 시 가끔 경찰차 시동이 안 걸리는 버그가 발생하는데, 다른 차량 안에 들어가 경찰차를 향해 총격을 가해 폭파 시킨뒤 수배를 풀고 계획실로 일정표를 가져오면 된다.
            <br><br>
            사실 경찰서 팀은 어떻게든 일정표만 훔쳐 나오면 되기 때문에 경찰차를 훔칠 필요 없이 바로 경찰서로 쳐들어가도 된다. 방탄 구루마를 타고 경찰서로 가 건물 안에 있는 경찰을 죽이고 1명은 구루마에 탄 채 입구에서 다른 경찰이 건물 안으로 들어 오지 못하게 어그로를 끌면, 다른 1명이 경찰서로 들어가 일정표를 챙긴 뒤 구루마를 타고 도주하는 방법이 있다. 이후 수배를 풀은 뒤 계획실로 일정표를 가져오면 끝. 경찰차를 훔칠 필요도, 파괴할 필요도 없기 때문에 매우 빠르게 클리어 할 수 있다. 시작할 때 화물선 팀에서 적절한 무기를 던져주면 매우 좋다. 단, 경찰을 잡느라 너무 오래 지체하면 추가로 지원 온 경찰의 수가 상당해지고 헤비 컴뱃이 아닌 일반 복장이라 구루마에 탑승하기 전에 사살될 위험이 있기 때문에 지능 범죄 도전시에는 자제하자.
