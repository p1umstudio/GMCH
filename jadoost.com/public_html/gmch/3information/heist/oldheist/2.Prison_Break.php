	플레이어들은 두 팀으로 나뉘게 된다. 경찰서에 잠입하여 일정표를 훔쳐오는 경찰서 팀, 항구로 가서 교수의 차량을 다시 빼오는 화물선 팀으로 나뉜다. 두 팀으로 나뉜 플레이어들은 한 쪽은 일정표를, 한 쪽은 차량을 가져온다. 경찰서팀은 경찰 크루저를 탈취해 경찰서로 간 뒤, 경찰서 내부로 잠입해 죄수 호송 일정표를 훔쳐서 리더의 아파트에 두면 끝. 화물선 팀은 화물선에 침입하여 갱단을 뚫고 래시코브스키의 자동차인 카스코를 가져와서 버스를 갖다 놓은 그 창고에 주차하면 끝이다.
	<br>
	이 계획준비에서는 3 알타 St. 아파트가 추천된다. 경찰서 팀은 경찰서와의 거리도 가깝고 아파트 앞에 지하철 터널도 뚫려 있어 경찰의 수배를 풀기에도 좋으며, 화물선 팀도 화물선까지 거리가 가까워서 좋다. 4 인테그리티 아파트도 입지 면에서 충분히 좋은 위치.

