        전투도 없고, 운전만 하면 되기 때문에 전투에 자신없다면 파일럿을 하는 것이 좋다. 단 착륙을 할 줄 몰라서 엔딩에 다와가는 순간인 지상 팀 태우러 착륙할 때 폭사할 경우 팀원들의 우레와 같은 탄성을 듣게되므로 꼭 연습해 보고 가는 것이 좋다.
        
    </dd>
    <dt>○파괴 요원○</dt>
    <dd>
        파괴요원은 죄수 호송버스를 따라가 버스 운전수를 죽인 뒤 버스를 탈취하면 된다. 온갖 이상한 이유로 죽거나 실패하는 GTA 특성상 교도관을 죽이면 버스가 언덕이나 벼랑으로 미끄러져 그대로 버스가 옆으로 전복되어 폭발해 실패하는 경우가 있고, 교도관을 죽이기 위해 쏜 총소리에 겁을 먹은 NPC들이 버스를 타기 위해 차에서 내린 유저를 뺑소니 치고 가는 어이없는 상황도 있으니 주의하자. 버스를 타고 활주로에 도착하면 점착폭탄 하나를 붙여 폭파하고 (단, 활주로 도착 이후, 정확한 지점에 도달하지 않고 차량을 폭파 시 (건물 뒤가 아닌 앞이라던지), 운반 도중 파괴된 것으로 간주되어 실패 처리되므로 주의.) 바로 옆에 있는 빨간색 오프로드를 타고 활주로 끝 너머 메리웨더(2명)가 지키는 버자드 헬기를 훔쳐서 밸럼과 지상팀을 지원하면 된다.
        <br><br>
        습격상황에 따라 파일럿을 지원할수도 있고 죄수팀을 지원할수도 있다. 플레이어의 유도리에 따라 헬기를 탈취하고 미션 내내 아무것도 안 하고 놀기만 하다가 마지막에 해안가로 낙하하는 팀원들을 태우고 가기만 할 수도 있지만 가급적 도와주는 것이 미션을 더 빨리 끝낼 수 있다. 파일럿이 먼저 언급한 제트기의 꼬리를 따라가는 트릭을 모른다면 밸럼 주변을 어슬렁거리다 제트기를 향해 미사일을 발사하면 된다. 밸럼이 제트기의 꽁무니를 잘 쫓아다닌다는 걸 확인하면 교도소 팀을 지원 할 수 있다. 다만 헬기가 교도소에 너무 가까이 접근하면 호밍 미사일에 격추 되기 때문에 버자드가 락온만 가능한 거리를 유지해 주는 것이 중요하다. 멀리 떨어져있어도 가끔식 호밍런쳐가 날아오니 이럴때는 회피기동을 써주자 전투기보다 피하기 어려울 것 같지만 방법을 알고나면 쉽고 버자드 자체가 회피기동하기도 쉬우니 익혀두자. 본인의 조종 실력이 매우 뛰어나다면 호밍 미사일도 피할 수는 있기 때문에 교도소 안까지 침입해 미사일을 피하며 적들을 잡고 시간 제한이 되기전에 밖으로 빠지는 식의 플레이도 가능하다. 이는 매우 위험한 방법이기 때문에 될 수 있으면 교도소 밖에서 지원해주는 것이 좋다. 죄수팀의 전투능력이 높든 낮든, 파괴 요원이 교도소를 지원해주는 것은 빠르고 안전한 진행에 매우 도움이 된다.
        <br><br>
        지원할 때 가장 쉬운 방법은, 파괴요원이 버자드를 타고 교도소 남쪽 담장 밖에 먼저 자리를 잡은 후에 교도관과 죄수가 교도소로 들어가면 된다. 이렇게 하면 교도관과 죄수가 체크포인트를 찍는 동안 나오는 모든 경찰을 파괴요원 혼자서 다 잡아버릴 수도 있다. 교도관과 죄수가 교수를 만나는 컷신이 나올 동안, 그들의 정면에 스폰되는 경찰들을 먼저 제거하는 것도 가능하다. 교도관과 죄수가 출구쪽으로 거의 다 오면, 파괴요원은 풍력 발전 단지의 중간쯤에서 대기하면 된다. 처음 스폰되는 경찰헬기 3대중 2대가 그쪽에서 스폰되기 때문에, 스폰 되자마자 바로 잡아버리면 된다. 그 후 교도소 팀과 거리를 유지하면서 스폰되는 경찰헬기를 모조리 격추하면 된다. 멀리서 쏴줘야 교도관과 죄수가 안전하게 벨럼에 탑승할 확률이 높아진다. 다만, 자칫 잘못하다 격추한 헬기의 잔해가 떨어져 밸럼 팀을 덮칠 수 있으니 격추할 때 조심해야 한다. 그 후, 교도소 팀이 교도소를 나와 밸럼을 타고 도망 가는 동안 파괴 요원은 밸럼 팀 근처로 오는 경찰들을 상대한다. 경찰 헬기는 사격거리가 길고 밸럼에 부딪힐 수도 있기 때문에 무조건 최우선 제거 대상이다. 계속 경찰헬기를 격추해서 경찰의 관심이 자신에게 집중되도록 해야 벨럼에 탄 나머지 3명이 수배 레벨을 떼기 수월해진다. 그리고 밸럼 팀이 도망가고 있을 때 따라가는 행동은 하지 말자. 경찰 어그로가 버자드에게 끌려있는 상태인데 지명수배를 떼야 하는 밸럼 멤버들에게 접근하는 것은 심각한 트롤링이 아닐 수 없다.
        <br><br>
        밸럼에 탄 멤버들이 수배령을 제거하면, 파괴요원 주변에 있던 경찰들이 사라지면서 수배가 금방 풀린다. 그 후, 해안가에 낙하한 팀원들을 버자드에 태우고 도시로 빠져나가면 된다. 파괴 요원은 미리 해변가에 헬기를 대놓고 기다릴 수도 있지만 엘리트 조건과는 상관 없기도 하거니와 낙하선을 타고 내려오다가 위치선정에 실패해서 헬기의 메인 로터에 갈려서 죽는다거나, 낙하하다 죽는 헬기에 부딪혀 죽는 경우도 있으므로 팀원의 수준을 생각해서 미리 착륙할 장소를 염두에 두자. 또 지상에 내려온 팀원이 헬기 테일로터에 갈려 죽는 경우도 많으므로 착륙 전에 미리 방향을 조절하자. 바위 위에 서 있다가 헬기를 향해 쓸데없이 점프해서 프로펠러에 갈려 죽는 일도 있으므로 미리 얘기해 줘야한다. 상공에서 팀원들이 모두 낙하한 것을 보고 천천히 착륙하면 된다.
        <br><br>
        파괴 요원은 팀원의 실력에 따라 해야 할 일의 편차가 큰 편이다. 파일럿이 제트기를 상대로 따라가는 트릭을 몰라서 회피기동에 실패하고 지상 팀 또한 자꾸 죽는다면 그야말로 파괴 요원의 캐리가 필요하다. 물론 버자드로 공중지원을 해줘도 교도소 팀이 죽는 경우도 있다. 반면에 팀원들의 실력이 걸출하면 파괴요원은 죄수 팀이 교수를 데리고 빠져나올 때까지 놀고 있어도 아무 문제없을 정도. 빠르면 버자드가 도착하기도 전에 이미 교도소를 빠져 나오는 팀원도 있으며 심지어 파일럿이 추격하는 제트기를 충분히 잘 다루고 경찰 헬기 또한 잘 피해 다닌다면 정말로 할게 없다.
        <br><br>
        요원 14는 너희들의 실력에 대해 감명 받았다면서 다음 계획을 예고하면서 계속 일해달라고 한다. 습격이 끝나면 플레이어들은 리더의 아파트에서 술잔으로 건배를 하며 하이파이브를 하고 파티를 하는 장면이 나온다.
        <br><br>
        하지만 실상은 많은 습격 뉴비들을 멘붕과 스트레스로 꽉 차게한 습격 피날레가 되어버렸다. 일단, 숙달된 유저끼리 한다면 피날레 자체의 난이도는 그렇게 크게 높지가 않다. 그게 한국인들끼리라면 더욱. 대놓고 돌진하는 죄수와 교도관만 없고, 파일럿은 그냥 속도 조절하며 제트기 뒤를 쫄래 쫄래 따라가고, 때맞춰서 이착륙만 잘하면 된다.근데도 멘붕에 빠지는 이유는 앞에도 수차례 언급되었던 수배레벨 버그 때문. 교수와 교도관과 죄수가 다같이 교도소를 탈출하고 밸럼을 다같이 타고 버자드가 엄호해주는 상황까지 오면 이 습격 자체는 이미 종반부이다. 밸럼에 탄 플레이어 3명은 경찰을 따돌려야 하는데 수배 버그 때문에 아무리 날아가도 경찰의 수배가 없어지지 않는다. 이런 버그는 고질적인 GTA 온라인의 문제였는데 그 버그가 이 미션에서 발암물질처럼 작용하기 때문에 많은 유저들이 항의했다.
        <br><br>
        이 버그가 발생하는 원인은 GTA 온라인의 시스템적인 문제에서 이해해야 하는데 수배가 걸린 유저 A와 수배가 걸리지 않은 B라는 유저가 있을 시 수배가 걸린 A가 탄 탑승물(차, 비행기, 헬기, 보트 등)에 수배가 안걸린 B가 탑승하게 되면, A의 수배령이 B에게도 적용 된다. 같은 탑승물에 여러 명이 타더라도 동시에 별이 떨어져야 하지만 A의 수배령이 떨어지려고 별이 깜빡일때 B의 수배령도 같이 떨어져야 하지만 버그로 인해 B의 수배령이 그대로 유지 되면 동승자 간에 수배령이 똑같이 적용되는 시스템상 B의 영향으로 깜빡이던 A의 수배령이 다시 불게 된다. 이후 B의 수배령이 떨어지려 할때도 수배령이 유지된 A의 영향으로 B의 수배령이 다시 붙는 무한 반복이 되는 것이 이 버그의 발생 방식. 이 버그의 원인은 유저들간의 인터넷 회선 상태가 좋지 않거나 핑이 높거나 탑승자가 많으면 많을수록 동승자간에 수배령이 동기화 되는데서 문제가 발생하기 때문이다. 특히 외국인 유저들과 플레이시 이 버그가 더욱 잘 생기는 편.
        <br><br>
        다른 습격이나 임무등에서 가장 좋은 해결 방법은 적당히 으슥한 곳에 탑승물을 대놓고 모두 내려 각자 별을 뗀 뒤 다시 타고 가는 것이지만 탈옥 피날레에서는 그럴수도 없기에 이를 위한 해결법이 여러가지가 있는데 몇 가지만 소개하자면.
        <ul>
            <li>파일럿이 저공 비행으로 경찰차나 헬기등의 시야에 일부러 계속 노출 되어 있다가 시야 밖으로 빠져서 수배령이 떨어지는 타이밍을 동시에 맞추는 방법이 있다.</li>
            <li>파괴요원이 계속해서 난동을 부려 수배령이 떨어지지 않게 유지하여, 경찰들의 주의를 끄는 동안 밸럼팀이 먼저 수배레벨을 떨어뜨리는 방법이 있다. 이 때에는 경찰의 시야가 버자드 쪽에 쏠리기 때문에 밸럼팀이 버그에 걸리더라도 버그에서 풀릴때까지 어느정도 시간을 벌 수 있다. 버자드 또한 굉장히 날렵하기 때문에 이후 무작정 고도만 높이지 않는다면 경찰 따돌리기가 매우 쉽다.</li>
            <li>사실 가장 확실한 방법으론 팀원들의 실력이 모두 상향 평준화 상태라면 피날레를 처음부터 다시 시작하는 방법이 있다. 다만 밸럼에 같이 탑승하는 교도관, 죄수, 파일럿 중 한명의 역할을 파괴요원과 바꿔야 한다. 위에서도 언급되었듯이 수배레벨 버그는 회선 상태가 좋지 않은 팀원이 두 명 이상이 같은 탑승물에 타서 수배레벨을 해제하지 못하는 상황이기 때문에 밸럼 팀 중 핑이 높은 한 명을 파괴요원과 역할을 바꿔서 수배령을 떼는 것이다.</li>
        </ul>
        <br>
        그 외, 저장 체크 포인트가 없다는 점도 멘붕에 빠지는데 한몫한다. 다른 습격 피날레들과 달리 탈옥 피날레는 어느 구간에서 실패하던 무조건 처음부터 다시 진행해야 하기 때문. 마지막 낙하 구간에서 프로펠러에 갈려죽거나 도시로 가는 헬기에서 뛰어내린 후 낙하산을 펴지않아 죽어도 얄짤 없다. 또한 다른 습격에서는 저장된 복장으로 맞출 수 있고, 꾸역꾸역 헤비 컴뱃을 입어주면서 플레이하기 때문에 사실상 습격 피날레 중에 가장 어렵다고 해도 무방하다. 숙달된 유저들 4명이 모여서 한다면 별 문제 없지만, 랜덤초대로 인원을 구성하여 시작한다면 모든 습격을 통틀어 성공 확률이 가장 떨어진다.
        <br><br>
