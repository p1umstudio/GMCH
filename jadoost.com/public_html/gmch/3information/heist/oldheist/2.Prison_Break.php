        <dt>○화물선 팀○</dt>
        <dd>
            화물선팀은 난이도가 좀 있다. 일단 항구로 가면 큰 배에 컨테이너가 수두룩 쌓여있는데 배 곳곳에 적들이 산개해있다.
            <br><br>
            침입 루트가 크게 2가지가 있다.
            <br>
            첫 번째 루트는 화물선 오른편에 있는 계단으로 올라가 정면 돌파하는 것이다.
            <br>
            계단에 있는 적들은 플레이어가 접근하면 무조건 총부터 쏜다. 스나이퍼 라이플로 한 명이라도 저격하면 바로 경보가 울리기 시작하니 잠입 액션은 애초에 시도도 할 수 없다. 계단 앞에 있는 적 2명을 제거한 뒤 배 측면에서 공격하는 적들을 제거하면 되는데, 적들이 난간을 이용해 엄폐를 하기 때문에 지상에서 총으로 잡는데 시간이 많이 걸리므로 지상에서 로켓을 이용해 측면 복도, 특히 컨테이너와 컨테이너 사이를 노려서 쏘면 상당수의 적들을 빠르게 정리 하는게 가능하다. 안전하게 클리어 하고 싶다면 구루마 안에서 배의 측면 복도의 적들을 모두 제거한 뒤 올라가면 되지만 시간이 오래 걸리는 단점이 있다. 어느정도 잡고 나면 계단을 올라 복도를 가로 질러 가면 되는데, 계단을 오를때 점프를 뛰면 난간을 타고 바다로 뛰어 들으니 주의. 중간중간 미처 다 처리하지 못한 적들을 잡으면서 복도 끝 뱃머리 쪽으로 진행한 뒤 컨테이너를 올라가면 된다. 헤비 컴뱃을 입고 있다면 죽을 일은 거의 없고, 헤비 컴뱃이 없더라도 엄폐만 잘 해주면 손쉽게 목표 지점에 도달할 수 있다.
            <br>
            다만 약간 변형적인 방법으로 왼쪽 복도를 향해 전진하는 것 보다 좀 더 쉬운 방법이 있다. 처음 왼쪽 복도로 은/엄폐하며 가지 않고 오른쪽 계단을 통해 한 층만 올라간 뒤에 배 안쪽으로 조금만 걸어가다 보면 컨테이너 위로 올라가는 통로가 있다. 통로를 통해 올라간 뒤에는 컨테이너 아래의 적들이 위쪽으로 사격하지 못하므로 플레이어는 컨테이너와 컨테이너 틈 사이로 빠지지 않고 적절히 뛰면서 가다가 자물쇠 근처의 적 2명만 잡으면 카스코까지 매우 안전하게 도달 할 수 있다.
            <br><br>
            두 번째 루트는 낙하산을 타고 컨테이너 위로 바로 착지하는 방법이다.
            <br>
            배를 바라봤을 때 뱃머리 쪽에 있는 크레인의 사다리 끝까지 올라간 뒤 준비되어있는 낙하산을 이용해 난간에서 컨테이너 위로 낙하하는 것이다. 높이가 낙하산을 펴기에 다소 아슬아슬하므로 난간에서 뛰자마자 곧바로 낙하산이 펴질 수 있게 광클하고 있는 것이 좋다. 계단 앞에 있던 놈들과는 달리 배 위에 있는 놈들은 사다리를 타는 플레이어가 분명히 시야에 들어오는데도 불구하고 먼저 쏘진 않는다. 일단 낙하를 하는 순간, 적들이 경보를 울리며, 컨테이너 위에 있는 적들만 처치하면 딱히 배 최상층까지 쫓아오는 갱단원은 없기 때문에 낙하산 펴는 것만 익숙하다면 첫 번째 방법보다 더 빠르다. 다만, 적들에게 발각 된 상태에서 낙하산을 타고 내려오면 좋은 과녁판이 될 수 있으니 주의.
            <br><br>
            컨테이너 위의 적을 모두 잡고 가운데 쯤에 있는 자물쇠가 걸린 컨테이너가 있는데 그 자물쇠를 누가 쏘느냐에 따라 카스코를 운전하는 사람이 갈린다. 운전에 자신 있는 사람이 쏘자. 자물쇠를 쏘면 컷신으로 넘어가는데 두 명 다 가까운 곳에 있다면 자동으로 둘 다 카스코 안으로 이동된다. 카스코는 방탄이 아니라서 공격에 굉장히 취약하니 주의해야 하며, 적들이 따라온다고 해서 차 안에서 총을 쏘게 되면 경찰의 수배가 걸리면서 노란색 GPS 경로도 안 보이게 되고 적들과 함께 경찰들까지 추격해 난이도가 급상승 하기 때문에 적들이 따라 붙더라도 총을 쏘지 않고 이동해야 한다.[17] 적들은 차를 타고 도로를 통해서 추격해 오기 때문에 도로를 따라 진행시 운전이 미숙해 어딘가에 잘못 박아서 끼거나 전복되면 그대로 포위 당해 사살될 수 있으니 주의하자.
            <br><br>
            적들이 귀찮다면, 아예 도로를 따라가지 않고 기찻길을 따라 쭉 진행하면 적들이 접근이 불가능해서 매우 안전하게 이동할 수 있다. 카스코를 몰고 나와 첫번째 바리케이드를 지난뒤 바로 왼쪽 기찻길을 따라가면 된다. 단, 반대 방향에서 오는 기차와 부딪힐 위험이 좀 있다. 기찻길에는 GPS가 찍히지 않으므로 기찻길을 따라 쭉 진행 해오다 오른쪽으로 고속도로가 나오는 지점에서 도로로 진입한뒤 GPS를 따라가면 된다. 이 방법은 안전하지만 다소 멀리 돌아가는 길이므로 참고 할 것.
            <br><br>
            화물선 팀의 목적은 어떻게든 카스코를 목표지점으로 가져가기만 하면 되기 때문에 1명만 카스코에 타고 가도 된다. 이 점을 응용해서 1명만 배로 먼저 가서 카스코를 훔치고, 다른 1명은 인서전트나 방탄 구루마에 타고 뒤따라 간 후, 배에서 좀 떨어진 곳에서 대기하다가 카스코가 배에서 내려오면 첫번째 바리케이드에서 길을 막고 있는 적들의 차를 들이받아서 튕겨내면 된다. 초보들이 보통 여기서 많이 죽는데, 이렇게 해서 길을 뚫어주면 죽을 위험도 줄어들고 카스코도 빠르게 주행할 수 있다. 카스코의 뒤를 따라가면서 적들이 따라붙는다 싶으면 계속 범퍼카 놀이를 해주면 된다. 앞부분을 들이받아서 방향만 틀어놔도 제대로 따라오지 못한다. 물론 총을 쏘면 안 된다.
            <br><br>
            카스코를 타고 적들을 따돌리며 고속도로를 달리다보면 요원 14가 무전기로 어디로 가고 있냐고 묻는다. 돌아서 가든 지름길로 가든 상관없이 물어본다. 그리곤 차를 망가뜨리지 말고 잘 타고 오라는 말과 함께, 나중에 그런차 마음껏 몰고 싶으면 스스로 돈 모아서 출세한 다음에 하라고 한다.
